# Dada una temperatura en grados celcius, obtener su equivalente en grados farenheit

print("Dada una temperatura en grados celcius, obtener su equivalente en grados farenheit")

celcius   = float(input("\nDame la temperatura en grados celcius: "))
farenheit = (celcius * 9/5) + 32
print(f"El equivalente a grados farenheint es: {farenheit}")

print(f"\nUna temperatura de {celcius} grados celsius, es equivalente a {farenheit} grados farenheit")
